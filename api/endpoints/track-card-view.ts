import type { FastifyReply, FastifyRequest } from 'fastify';
import { trackCardView } from '../../db/db-card-views-utils.ts';
import { sendMotivationalMessage } from '../../bot/bot-message-utils.ts';

type TrackCardViewBody = {
  card_id: string;
  user_id: number;
};

// –ü–æ—Ä–æ–≥–∏ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –º–æ—Ç–∏–≤–∞—Ü–∏–æ–Ω–Ω—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π
const MOTIVATION_THRESHOLDS = [3, 5, 10, 20];

/**
 * –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –º–æ—Ç–∏–≤–∞—Ü–∏–æ–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–∞ –æ—Å–Ω–æ–≤–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –ø—Ä–æ—Å–º–æ—Ç—Ä–æ–≤.
 */
function getMotivationalMessage(viewCount: number): string {
  if (viewCount === 3) {
    return 'üéâ –û—Ç–ª–∏—á–Ω–æ! –í—ã —É–∂–µ –ø—Ä–æ—Å–º–æ—Ç—Ä–µ–ª–∏ 3 –∏–Ω–∏—Ü–∏–∞—Ç–∏–≤—ã. –ü—Ä–æ–¥–æ–ª–∂–∞–π—Ç–µ –∏—Å—Å–ª–µ–¥–æ–≤–∞—Ç—å –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–∏ –ø–æ–º–æ—á—å!';
  } else if (viewCount === 5) {
    return 'üåü –ü—Ä–µ–≤–æ—Å—Ö–æ–¥–Ω–æ! 5 –ø—Ä–æ—Å–º–æ—Ç—Ä–µ–Ω–Ω—ã—Ö –∏–Ω–∏—Ü–∏–∞—Ç–∏–≤ ‚Äî –≤—ã –Ω–∞ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–º –ø—É—Ç–∏ –∫ –¥–æ–±—Ä—ã–º –¥–µ–ª–∞–º!';
  } else if (viewCount === 10) {
    return 'üí´ –ù–µ–≤–µ—Ä–æ—è—Ç–Ω–æ! 10 –∏–Ω–∏—Ü–∏–∞—Ç–∏–≤ ‚Äî –≤—ã –Ω–∞—Å—Ç–æ—è—â–∏–π –∞–∫—Ç–∏–≤–∏—Å—Ç –¥–æ–±—Ä–∞! –°–ø–∞—Å–∏–±–æ –∑–∞ –≤–∞—à—É –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å!';
  } else if (viewCount === 20) {
    return 'üèÜ –ü–æ—Ç—Ä—è—Å–∞—é—â–µ! 20 –∏–Ω–∏—Ü–∏–∞—Ç–∏–≤ ‚Äî –≤—ã –≤–¥–æ—Ö–Ω–æ–≤–ª—è–µ—Ç–µ –¥—Ä—É–≥–∏—Ö –Ω–∞ –¥–æ–±—Ä—ã–µ –¥–µ–ª–∞! –ü—Ä–æ–¥–æ–ª–∂–∞–π—Ç–µ –≤ —Ç–æ–º –∂–µ –¥—É—Ö–µ!';
  }
  return `üëç –°–ø–∞—Å–∏–±–æ –∑–∞ –∏–Ω—Ç–µ—Ä–µ—Å –∫ –∏–Ω–∏—Ü–∏–∞—Ç–∏–≤–∞–º! –í—ã —É–∂–µ –ø—Ä–æ—Å–º–æ—Ç—Ä–µ–ª–∏ ${viewCount} –∫–∞—Ä—Ç–æ—á–µ–∫.`;
}

/**
 * –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç POST /track-card-view.
 * 
 * –ü—Ä–∏–Ω–∏–º–∞–µ—Ç JSON body —Å –ø–æ–ª—è–º–∏:
 * - card_id (–æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ–µ): ID –∫–∞—Ä—Ç–æ—á–∫–∏, –∫–æ—Ç–æ—Ä—É—é –ø—Ä–æ—Å–º–æ—Ç—Ä–µ–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å
 * - user_id (–æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ–µ): ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è MAX
 * 
 * –°–æ—Ö—Ä–∞–Ω—è–µ—Ç –∏–ª–∏ –æ–±–Ω–æ–≤–ª—è–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø—Ä–æ—Å–º–æ—Ç—Ä–µ –∫–∞—Ä—Ç–æ—á–∫–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º –≤ –ë–î.
 * –ï—Å–ª–∏ –ø—Ä–æ—Å–º–æ—Ç—Ä —É–∂–µ –±—ã–ª —Å–æ—Ö—Ä–∞–Ω—ë–Ω —Ä–∞–Ω–µ–µ, –æ–±–Ω–æ–≤–ª—è–µ—Ç –≤—Ä–µ–º—è –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –ø—Ä–æ—Å–º–æ—Ç—Ä–∞ –∏ —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç —Å—á—ë—Ç—á–∏–∫.
 * –ü—Ä–∏ –¥–æ—Å—Ç–∏–∂–µ–Ω–∏–∏ –æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω—ã—Ö –ø–æ—Ä–æ–≥–æ–≤ (3, 5, 10, 20 –ø—Ä–æ—Å–º–æ—Ç—Ä–æ–≤) –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –º–æ—Ç–∏–≤–∞—Ü–∏–æ–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ.
 *
 * –£—Å–ø–µ—Ö: –æ—Ç–¥–∞—ë—Ç 200 —Å –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ–º { ok: true, view_count: number }.
 * –û—à–∏–±–∫–∞: –ª–æ–≥–∏—Ä—É–µ—Ç –ø—Ä–∏—á–∏–Ω—É –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç 400/500 —Å —Ç–µ–∫—Å—Ç–æ–º –æ—à–∏–±–∫–∏.
 */
export async function handleTrackCardView(req: FastifyRequest<{ Body: TrackCardViewBody }>, reply: FastifyReply) {
  try {
    const { card_id, user_id } = req.body;

    if (!card_id || !user_id) {
      return reply.code(400).send({ ok: false, error: 'card_id and user_id are required' });
    }

    const viewCount = await trackCardView(card_id, user_id);

    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –º–æ—Ç–∏–≤–∞—Ü–∏–æ–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –ø—Ä–∏ –¥–æ—Å—Ç–∏–∂–µ–Ω–∏–∏ –ø–æ—Ä–æ–≥–æ–≤
    if (MOTIVATION_THRESHOLDS.includes(viewCount)) {
      const message = getMotivationalMessage(viewCount);
      // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω–æ, –Ω–µ –±–ª–æ–∫–∏—Ä—É—è –æ—Ç–≤–µ—Ç
      sendMotivationalMessage(user_id, message).catch((err) => {
        req.log.error('Failed to send motivational message:', err);
        // –ù–µ –ø—Ä–æ–±—Ä–∞—Å—ã–≤–∞–µ–º –æ—à–∏–±–∫—É, —á—Ç–æ–±—ã –Ω–µ –Ω–∞—Ä—É—à–∏—Ç—å –æ—Å–Ω–æ–≤–Ω–æ–π –ø–æ—Ç–æ–∫
      });
    }

    return reply.code(200).send({ ok: true, view_count: viewCount });
  } catch (e: any) {
    req.log.error(e);
    return reply.code(500).send({ ok: false, error: e?.message ?? 'Unknown error' });
  }
}

